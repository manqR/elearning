<?php

/* @var $this \yii\web\View */
/* @var $content string */

use yii\helpers\Html;
use yii\bootstrap\Nav;
use yii\bootstrap\NavBar;
use yii\widgets\Breadcrumbs;
use frontend\assets\AppAsset;
use common\widgets\Alert;
use frontend\models\RolePrivillage;   
use frontend\models\Role;
use frontend\models\Menu;

if (!Yii::$app->user->isGuest) {
    $role = Role::find()
    ->where(['idrole'=>Yii::$app->user->identity->roleID])
    ->One();
}


AppAsset::register($this);
?>
<?php $this->beginPage() ?>
<!DOCTYPE html>
<html lang="<?= Yii::$app->language ?>">
<head>
    <meta charset="<?= Yii::$app->charset ?>">
    <meta http-equiv="X-UA-Compatible" content="IE=edge">
    <meta name="viewport" content="width=device-width, initial-scale=1">
    <?php $this->registerCsrfMetaTags() ?>
    <title><?= Html::encode($this->title) ?></title>
    <?php $this->head() ?>
</head>
<body>
<?php $this->beginBody() ?>

<div class="wrap">
    <?php
    NavBar::begin([
        'brandLabel' => Yii::$app->name,
        'brandUrl' => Yii::$app->homeUrl,
        'options' => [
            'class' => 'navbar-inverse navbar-fixed-top',
        ],
    ]);
    $menuItems = array();
    if (Yii::$app->user->isGuest) {
        $menuItems[] = ['label' => 'Signup', 'url' => ['/site/signup']];
        $menuItems[] = ['label' => 'Login', 'url' => ['/site/login']];
    } else {

        $model = Menu::find()
            ->where(['flag'=>1])
            ->all();

        $x = 0; 
        $output = [];       
        foreach($model as $models):
            $x += 1;
            $privileges = RolePrivillage::find()
                ->where(['like', 'menu_name', $models->nama_menu])
                ->AndWhere(['description'=>'HEAD'])
                ->AndWhere(['idrole'=>Yii::$app->user->identity->roleID])
                ->One();
            
            if($privileges){
                if($privileges->flag == 1){                    
                    $menuItems[] = array(
                        'label' => $models->nama_menu,
                        'url'=> '?r='.$models->link,
                        'active' => $this->context->route == $models->link,
                        
                    );
                    
                }
            }

        endforeach;     
     
        $menuItems[] = '<li>'
            . Html::beginForm(['/site/logout'], 'post')
            . Html::submitButton(
                'Logout (' . Yii::$app->user->identity->username . ')',
                ['class' => 'btn btn-link logout']
            )
            . Html::endForm()
            . '</li>';
    }
    echo Nav::widget([
        'options' => ['class' => 'navbar-nav navbar-right'],
        'items' => $menuItems,
    ]);
    NavBar::end();
    ?>

    <div class="container">
        <?= Breadcrumbs::widget([
            'links' => isset($this->params['breadcrumbs']) ? $this->params['breadcrumbs'] : [],
        ]) ?>
        <?= Alert::widget() ?>
        <?= $content ?>
    </div>
